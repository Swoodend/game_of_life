  componentWillMount(){
    let randomState = getRandomState(this.state.cellGrid);
    this.setState({
      cellGrid: randomState
    });
  }

  changeColor(row, column){
    let grid = this.state.cellGrid;

    if (grid[row][column] === 1){
      grid[row][column] = 0;
    } else {
      grid[row][column] = 1;
    }

    this.setState({
      cellGrid: grid
    });
  }

  render() {
    let cells = this.state.cellGrid.map((cellRow, i) => {
      return cellRow.map((cellColumn, j) => {
        if (cellColumn){
          return (
            <Cell
              changeColor={this.changeColor}
              key={i+j}
              row={i}
              column={j}
              style={{background: "pink"}}
            />
          );
        } else {
          return (
            <Cell
              changeColor={this.changeColor}
              key={i+j}
              row={i}
              column={j}
            />
          );
        }
      })
    })
    return (
      <div className="board">
        {cells}
      </div>
    );
  }
